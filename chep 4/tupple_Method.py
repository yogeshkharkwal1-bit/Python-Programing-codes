# count()
# Returns the number of times a specified value occurs in the tuple.

t= (1, 2, 3, 2, 4, 2)
print(t.count(2))   # Output: 3


# index()
# Returns the first index of the specified value.
# Raises an error if the value is not found.

t = (10, 20, 30, 20, 40)
print(t.index(20))  # Output: 1

t = (5, 2, 9, 1) 


print(len(t))      # 4
print(max(t))      # 9
print(min(t))      # 1
print(sum(t))      # 17
print(sorted(t))   # [1, 2, 5, 9]
